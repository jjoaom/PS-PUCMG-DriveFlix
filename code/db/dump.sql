DROP TABLE IF EXISTS public.users CASCADE;

CREATE TABLE public.users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email VARCHAR(255),
    name VARCHAR(255),
    password VARCHAR(255)
);

CREATE TABLE public.agentes (
    id BIGINT PRIMARY KEY,
    cnpj VARCHAR(255),
    razao_social VARCHAR(255),
    tipo VARCHAR(255),
    CONSTRAINT fk_agentes_user
        FOREIGN KEY (id)
        REFERENCES public.users (id)
        ON DELETE CASCADE
);

CREATE TABLE public.clients (
    id BIGINT PRIMARY KEY,
    address VARCHAR(255),
    cpf VARCHAR(255),
    name VARCHAR(255),
    phone VARCHAR(255),
    rg VARCHAR(255),
    renda DOUBLE PRECISION,
    CONSTRAINT fk_clients_user
        FOREIGN KEY (id)
        REFERENCES public.users (id)
        ON DELETE CASCADE
);

CREATE TABLE public.carros (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    imagem_url VARCHAR(255),
    marca VARCHAR(255),
    modelo VARCHAR(255),
    placa VARCHAR(255),
    preco DOUBLE PRECISION NOT NULL,
    status VARCHAR(255),
    agent_id BIGINT NOT NULL,
    CONSTRAINT fk_carros_agent
        FOREIGN KEY (agent_id)
        REFERENCES public.agentes (id)
);

CREATE TABLE public.pedidos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    car_id BIGINT NOT NULL,
    client_id BIGINT NOT NULL,
    data_criacao TIMESTAMP(6) WITHOUT TIME ZONE,
    parecer_financeiro VARCHAR(255),
    status VARCHAR(255),
    CONSTRAINT fk_pedidos_car
        FOREIGN KEY (car_id)
        REFERENCES public.carros (id),
    CONSTRAINT fk_pedidos_client
        FOREIGN KEY (client_id)
        REFERENCES public.clients (id)
);

